package chapter02;

public class Java03_Datatype_1 {
    //todo 1.整数类型
    //byte：8位
    byte b =10;

    //short：16位
    short s =10;
    //int：32位
    int i =10;
    //long：64位
    long lon =10;

    //todo 2.浮点类型：含有小数点的数据类型
    //根据计算精度分
    //默认情况下，小数点的数据会被识别为精度较高的双精度double类型
    //float：单精度浮点类型，数据需要使用F（f）结尾
    float f =1.0f;
    //double：双精度浮点类型
    double d =2.0;

    //todo 3.字符类型
    //所谓的字符类型，其实就是使用符号标识文字内容
    char c ='@';

    //todo 4.布尔类型
    boolean bo =true;
}
